package com.example.shopguideagent.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Modern corner radius tokens.
 * Uses a more varied scale for visual hierarchy.
 */
object AppCornerRadius {
    /** Small controls: text field inner, small buttons */
    val Small = 12.dp

    /** Controls: buttons, chips, badges */
    val Control = 16.dp

    /** Buttons */
    val Button = 16.dp

    /** Input fields */
    val Input = Control

    /** Cards and small surfaces */
    val Card = 20.dp

    /** Large cards, dialogs */
    val LargeCard = 24.dp

    /** Bottom sheets, modals */
    val Sheet = 28.dp

    /** Full rounded (pill shape) */
    val Pill = 999.dp
}
